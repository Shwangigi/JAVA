package cho2;

public class LongExam {

	public static void main(String[] args) {
		// long 타입은 int타입의 2배인 64bit 값을 갖는다.
		// 대부분 금융 , 은행 , 증권 등 돈에 관련된 변수타입으로 사용됨
		// 초기값 입력시 int와 차이를 주기 위헤 숫자 뒤에 i,L을 붙인다.
		
		long var1= 10;  //int 범위안엔서는 생략  가능
		long var2 = 20L;
		long var3 = 2222222222222222222L ; // int is out of range 뒤에 I,L을 안 붙이려면 int 범위안에서해야됨
		

	}

}
